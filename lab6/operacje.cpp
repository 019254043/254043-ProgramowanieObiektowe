#include "funkcje2.h"

float Tablica_Liczby::operacje(int *wybor, int n, int m, float wynik)
{
	if(n > wiersz || m > kolumna)
	{
   		return ERR_INVALID_SIZE;
   	}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	if(*wybor==1)								//Suma wiersza
	{
		for(int i=0; i < (kolumna); i++)				
		{
			wynik += tab[n-1][i];
		}		
	}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	else if(*wybor==2)								//Suma kolumny
	{
		for(int i=0; i < (wiersz); i++)				
		{
			wynik += tab[i][m-1];
		}		
	}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	else if(*wybor==3)        							  // Największa wartość w konkretnym wierszu
	{	
		wynik = tab[n-1][0];
		for(int i=1; i < kolumna; i++)
		{	
			if(wynik < tab[n-1][i])
			{
				wynik = tab[n-1][i];
			}
		}	
	}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	else if(*wybor==4)          							 // Największa wartość w konkretnej kolumnie
	{
		wynik = tab[0][m-1];
		for(int i=1; i < wiersz; i++)
		{	
			if(wynik < tab[i][m-1])
			{
				wynik = tab[i][m-1];
			}
		}
	}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	else if(*wybor==5) 								// Najmniejsza wartość w konkretnym wierszu
	{
		wynik = tab[n-1][0];
		for(int i=1; i < kolumna; i++)
		{
			if(wynik > tab[n-1][i])
			{
				wynik = tab[n-1][i];
			}
		}	
	}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	if(*wybor==6)								// Najmniejsza wartość w konkretnej kolumnie
	{
		wynik = tab[0][m-1];
		for(int i=1; i < wiersz; i++)
		{	
			if(wynik > tab[i][m-1])
			{
				wynik = tab[i][m-1];
			}
		}
	}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	else if(*wybor==7)          							 // Średnia wartość w konkretnym wierszu
	{	
		for(int i=0; i< kolumna; i++)
		{
			 wynik += tab[n-1][i];
		}
		wynik = wynik / kolumna;
	}	
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	else if(*wybor==8)       					    		// Średnia wartość w konkretnej kolumnie
	{	
		for(int i=0; i< wiersz; i++)
		{
			 wynik += tab[i][m-1];
		}
		wynik = wynik / wiersz;
	}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	return wynik;
}

